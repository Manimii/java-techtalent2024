package t04;

public class Ejercicio3App {

	public static void main(String[] args) {

		int x = 10, y = 5;
		double n = 10.5, m = 5.5;

		System.out.println(
				"Variable X = " + x + " Variable Y = " + y + " Variable N = " + n + " Variable M = " + m + "\n");

		System.out.println("Suma X+Y = " + (x + y));
		System.out.println("Diferencia X-Y = " + (x - y));
		System.out.println("Producto X*Y = " + (x * y));
		System.out.println("Cociente X/Y = " + (x / y));
		System.out.println("Resto X%Y = " + (x % y) + "\n");

		System.out.println("Suma N+M = " + (n + m));
		System.out.println("Diferencia N-M = " + (n - m));
		System.out.println("Producto N*M = " + (n * m));
		System.out.println("Cociente N/M = " + (n / m));
		System.out.println("Resto N%M = " + (n % m) + "\n");

		System.out.println("Doble de X = " + (x * 2));
		System.out.println("Doble de Y = " + (y * 2));
		System.out.println("Doble de N = " + (n * 2));
		System.out.println("Doble de M = " + (m * 2) + "\n");

		System.out.println("Suma de X+Y+N+M = " + (x + y + n + m));
		System.out.println("Producto de X*Y*N*M = " + (x * y * n * m));

	}

}
